import metadata from "./bags-mint-metadata.json";

export type BagsMintMeta = {
  mint: string;
  name: string | null;
  symbol: string | null;
  description: string | null;
  image: string | null;
  /** Path under /public — set once images are downloaded locally. */
  localImage?: string | null;
};

const META_BY_MINT = new Map<string, BagsMintMeta>(
  (metadata as BagsMintMeta[]).map((m) => [m.mint, m]),
);

/**
 * Real name/symbol/image for a given Bags mint — null if unseeded.
 * The `image` field is ONLY ever a local /public path (or null). Remote URLs
 * are deliberately dropped so the UI never renders a broken IPFS/Helius CDN
 * image — missing images fall through to the initials placeholder.
 */
export function getMintMeta(mint: string): BagsMintMeta | null {
  const raw = META_BY_MINT.get(mint);
  if (!raw) return null;
  return { ...raw, image: raw.localImage ?? null };
}

/** Real ticker if we have metadata, otherwise first 4 chars of the mint. */
export function mintTicker(mint: string): string {
  const m = META_BY_MINT.get(mint);
  return (m?.symbol?.trim() || mint.slice(0, 4)).toUpperCase();
}

/** Real token name if we have it, otherwise the short mint. */
export function mintName(mint: string): string {
  const m = META_BY_MINT.get(mint);
  return m?.name?.trim() || shortMint(mint, 6, 6);
}

/** Short "XXXX…YYYY" representation of a mint for badges. */
export function shortMint(mint: string, head = 4, tail = 4): string {
  if (mint.length <= head + tail + 1) return mint;
  return `${mint.slice(0, head)}…${mint.slice(-tail)}`;
}

export function isBagsMint(value: string): boolean {
  return BAGS_MINTS.includes(value);
}

const BASE58_RX = /^[1-9A-HJ-NP-Za-km-z]+$/;

/**
 * Loose check that a string could plausibly be a Solana SPL token mint
 * address — base58 alphabet, 32–44 chars. We stay permissive on purpose
 * so fresh (un-seeded) Bags mints still render their /t/[mint] page.
 */
export function isLikelySolanaMint(value: string): boolean {
  if (value.length < 32 || value.length > 44) return false;
  return BASE58_RX.test(value);
}
